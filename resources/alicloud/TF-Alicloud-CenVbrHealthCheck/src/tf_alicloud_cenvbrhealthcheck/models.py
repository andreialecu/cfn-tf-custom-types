@dataclass
class ResourceModel(BaseModel):
    tfcfnid: Optional[str]
    CenId: Optional[str]
    HealthCheckInterval: Optional[float]
    HealthCheckSourceIp: Optional[str]
    HealthCheckTargetIp: Optional[str]
    HealthyThreshold: Optional[float]
    Id: Optional[str]
    VbrInstanceId: Optional[str]
    VbrInstanceOwnerId: Optional[float]
    VbrInstanceRegionId: Optional[str]
    Timeouts: Optional["_TimeoutsDefinition"]

    @classmethod
    def _deserialize(
        cls: Type["_ResourceModel"],
        json_data: Optional[Mapping[str, Any]],
    ) -> Optional["_ResourceModel"]:
        if not json_data:
            return None
        dataclasses = {n: o for n, o in getmembers(sys.modules[__name__]) if isclass(o)}
        recast_object(cls, json_data, dataclasses)
        return cls(
            tfcfnid=json_data.get("tfcfnid"),
            CenId=json_data.get("CenId"),
            HealthCheckInterval=json_data.get("HealthCheckInterval"),
            HealthCheckSourceIp=json_data.get("HealthCheckSourceIp"),
            HealthCheckTargetIp=json_data.get("HealthCheckTargetIp"),
            HealthyThreshold=json_data.get("HealthyThreshold"),
            Id=json_data.get("Id"),
            VbrInstanceId=json_data.get("VbrInstanceId"),
            VbrInstanceOwnerId=json_data.get("VbrInstanceOwnerId"),
            VbrInstanceRegionId=json_data.get("VbrInstanceRegionId"),
            Timeouts=TimeoutsDefinition._deserialize(json_data.get("Timeouts")),
        )


