T = TypeVar("T")


def set_or_none(value: Optional[Sequence[T]]) -> Optional[AbstractSet[T]]:
    if value:
        return set(value)
    return None


@dataclass
class ResourceHandlerRequest(BaseResourceHandlerRequest):
    # pylint: disable=invalid-name
    desiredResourceState: Optional["ResourceModel"]
    previousResourceState: Optional["ResourceModel"]


@dataclass
class ResourceModel(BaseModel):
    tfcfnid: Optional[str]
    Dimensions: Optional[Sequence["_DimensionsDefinition"]]
    Force: Optional[bool]
    Id: Optional[str]
    Limit: Optional[str]
    Metric: Optional[str]
    Name: Optional[str]
    OverrideValue: Optional[str]
    Project: Optional[str]
    Service: Optional[str]
    Timeouts: Optional["_TimeoutsDefinition"]

    @classmethod
    def _deserialize(
        cls: Type["_ResourceModel"],
        json_data: Optional[Mapping[str, Any]],
    ) -> Optional["_ResourceModel"]:
        if not json_data:
            return None
        dataclasses = {n: o for n, o in getmembers(sys.modules[__name__]) if isclass(o)}
        recast_object(cls, json_data, dataclasses)
        return cls(
            tfcfnid=json_data.get("tfcfnid"),
            Dimensions=deserialize_list(json_data.get("Dimensions"), DimensionsDefinition),
            Force=json_data.get("Force"),
            Id=json_data.get("Id"),
            Limit=json_data.get("Limit"),
            Metric=json_data.get("Metric"),
            Name=json_data.get("Name"),
            OverrideValue=json_data.get("OverrideValue"),
            Project=json_data.get("Project"),
            Service=json_data.get("Service"),
            Timeouts=TimeoutsDefinition._deserialize(json_data.get("Timeouts")),
        )


# work around possible type aliasing issues when variable has same name as a model
_ResourceModel = ResourceModel


