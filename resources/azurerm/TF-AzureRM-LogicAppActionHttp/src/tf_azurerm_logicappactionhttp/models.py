@dataclass
class ResourceModel(BaseModel):
    tfcfnid: Optional[str]
    Body: Optional[str]
    Headers: Optional[Sequence["_HeadersDefinition"]]
    Id: Optional[str]
    LogicAppId: Optional[str]
    Method: Optional[str]
    Name: Optional[str]
    Uri: Optional[str]
    RunAfter: Optional[Sequence["_RunAfterDefinition"]]
    Timeouts: Optional["_TimeoutsDefinition"]

    @classmethod
    def _deserialize(
        cls: Type["_ResourceModel"],
        json_data: Optional[Mapping[str, Any]],
    ) -> Optional["_ResourceModel"]:
        if not json_data:
            return None
        dataclasses = {n: o for n, o in getmembers(sys.modules[__name__]) if isclass(o)}
        recast_object(cls, json_data, dataclasses)
        return cls(
            tfcfnid=json_data.get("tfcfnid"),
            Body=json_data.get("Body"),
            Headers=deserialize_list(json_data.get("Headers"), HeadersDefinition),
            Id=json_data.get("Id"),
            LogicAppId=json_data.get("LogicAppId"),
            Method=json_data.get("Method"),
            Name=json_data.get("Name"),
            Uri=json_data.get("Uri"),
            RunAfter=deserialize_list(json_data.get("RunAfter"), RunAfterDefinition),
            Timeouts=TimeoutsDefinition._deserialize(json_data.get("Timeouts")),
        )


# work around possible type aliasing issues when variable has same name as a model
_ResourceModel = ResourceModel


